#include <iostream>
using namespace std;

int main()
{
    /*
    比较运算符：== != < > <= >=
    */
    cout << (1 == 1) << endl; // 1
    cout << (1 != 2) << endl; // 1
    cout << (1 < 2) << endl;  // 1
    cout << (1 > 2) << endl;  // 0
    cout << (1 <= 2) << endl; // 1
    cout << (1 >= 2) << endl; // 0
    return 0;
}